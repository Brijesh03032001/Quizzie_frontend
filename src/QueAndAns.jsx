import React, { useState } from 'react';
import './QueAndAns.css'; // Import your CSS file for styling

function QueAndAns() {
  const [quizName, setQuizName] = useState('');
  const [optionType, setOptionType] = useState('Text');
  const [defaultValues, setDefaultValues] = useState(['text', 'text', 'text', 'text']);

  const handleOptionTypeChange = (type) => {
    setOptionType(type);

    if (type === 'Text') {
      setDefaultValues(['text', 'text', 'text', 'text']);
    } else if (type === 'Image URL') {
      setDefaultValues(['Image URL', 'Image URL', 'Image URL', 'Image URL']);
    } else if (type === 'Text and Image URL') {
      setDefaultValues(['text', 'text', 'Image URL', 'Image URL']);
    }
  };

  const handleOptionValueChange = (index, value) => {
    const newDefaultValues = [...defaultValues];
    newDefaultValues[index] = value;
    setDefaultValues(newDefaultValues);
  };

  const handleQuizNameChange = (event) => {
    setQuizName(event.target.value);
  };

  return (
    <div className="Dashboard_questionModalOverlay__OGshy">
      <div className="Dashboard_questionModal__Eb-eQ">
        <div className="Dashboard_modalContent__BmdQm">
          <div style={{ display: 'flex', alignItems: 'center', justifyContent: 'space-between' }}>
            <div style={{ display: 'flex', gap: '0.5rem', alignItems: 'center' }}>
              <div className="Dashboard_questionNo__IcRU4 Dashboard_activeQuestionNumber__OK7hC">1</div>
              <div className="Dashboard_addBtn__d4PQa">+</div>
            </div>
            <p>Max 5 Questions</p>
          </div>
          <div className="Dashboard_questionContent__9hHzv">
            <div>
              <input
                type="text"
                placeholder="Quiz Question"
                className="Dashboard_pollQuestion__HeJlM"
                value={quizName}
                onChange={handleQuizNameChange}
              />
            </div>
            <div style={{ display: 'flex' }}>
              <div style={{ marginRight: '1.5rem' }}>Option Type:</div>
              <label style={{ display: 'flex', alignItems: 'center' }} className='OptionLabel'>
                <input
                  type="radio"
                  name="optionType"
                  className='OptionInput'
                  checked={optionType === 'Text'}
                  onChange={() => handleOptionTypeChange('Text')}
                />
                Text
              </label>
              <label style={{ display: 'flex', alignItems: 'center', marginLeft: '0.5rem' }} className='OptionLabel'>
                <input
                  type="radio"
                  name="optionType"
                  className='OptionInput'
                  checked={optionType === 'Image URL'}
                  onChange={() => handleOptionTypeChange('Image URL')}
                />
                Image URL
              </label>
              <label style={{ display: 'flex', alignItems: 'center', marginLeft: '0.5rem' }} className='OptionLabel'>
                <input
                  type="radio"
                  name="optionType"
                  className='OptionInput'
                  checked={optionType === 'Text and Image URL'}
                  onChange={() => handleOptionTypeChange('Text and Image URL')}
                />
                Text and Image URL
              </label>
            </div>
            <div className = "OptionsAndTimer_ew">
              <div style={{ display: 'flex', flexDirection: 'column', flex: 1 }}>
                {defaultValues.map((defaultValue, index) => (
                  <div key={index}>
                    <input
                      type="radio"
                      name="ansOption"
                      className="DashboardImageText"
                      onChange={() => handleOptionValueChange(index, 'text')}
                    />
                    <input
                      type="text"
                      name={`optionText_${index}`}
                      placeholder="Option"
                      className="Dashboard_optionInput__hvDuj"
                      value={defaultValue}
                      onChange={(e) => handleOptionValueChange(index, e.target.value)}
                    />
                   
                  </div>
                ))}
              </div>
              <div style={{ display: 'flex', flexDirection: 'column', justifyContent: 'flex-end' }}>
                <div style={{ margin: 'auto', fontWeight: 'bold', marginBottom: '0.5rem' }}>Timer</div>
                <label className="TimerTypeInput">
                  <input type="radio" name="timerType" value="5 Sec" />
                  5 Sec
                </label>
                <label className="TimerTypeInput">
                  <input type="radio" name="timerType" value="10 Sec" />
                  10 Sec
                </label>
                <label className="TimerTypeInput">
                  <input type="radio" name="timerType" value="OFF" />
                  OFF
                </label>
              </div>
            </div>
            <div className="Dashboard_buttonContainer__jYQEj">
              <button className="Dashboard_cancelModalButton__XBrnr">Cancel</button>
              <button
                className="Dashboard_confirmCreateQuizButton__fPfr"
               
              >
                Create Quiz
              </button>
            </div>
          </div>
        </div>
      </div>
    </div>
  );
}

export default QueAndAns;
